// Function to generate random attendance status
function generateAttendanceStatus() {
    const statuses = ['Present', 'Absent'];
    return statuses[Math.floor(Math.random() * statuses.length)];
}

// Function to generate random date within a specified range
function generateRandomDate(start, end) {
    return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()));
}

// Generate attendance records for each student
function generateAttendanceRecords(students) {
    const attendanceRecords = [];
    for (const student of students) {
        const status = generateAttendanceStatus();
        const date = generateRandomDate(new Date(2024, 3, 1), new Date()); // Random date within April 2024
        const attendanceRecord = {
            "class": "661b289bb50ece57f8566803",
            "student": student.student._id,
            "status": status,
            "date": date
        };
        attendanceRecords.push(attendanceRecord);
    }
    return attendanceRecords;
}

